package com.raoulvdberge.refinedstorage.item;

import com.raoulvdberge.refinedstorage.RS;
import com.raoulvdberge.refinedstorage.api.network.item.INetworkItem;
import com.raoulvdberge.refinedstorage.api.network.item.INetworkItemHandler;
import com.raoulvdberge.refinedstorage.apiimpl.network.item.NetworkItemWirelessCraftingGrid;
import com.raoulvdberge.refinedstorage.item.info.ItemInfo;
import com.raoulvdberge.refinedstorage.render.IModelRegistration;
import net.minecraft.client.renderer.block.model.ModelResourceLocation;
import net.minecraft.entity.player.EntityPlayer;
import net.minecraft.item.ItemStack;
import net.minecraftforge.fml.relauncher.Side;
import net.minecraftforge.fml.relauncher.SideOnly;

import javax.annotation.Nonnull;

public class ItemWirelessCraftingGrid extends ItemWirelessGrid {

    public ItemWirelessCraftingGrid() {
        super(new ItemInfo(RS.ID, "wireless_crafting_grid"));
        this.setCreativeTab(RS.INSTANCE.tab);
    }

    @Override
    @SideOnly(Side.CLIENT)
    public void registerModels(IModelRegistration modelRegistration) {
        modelRegistration.setModel(this, 0, new ModelResourceLocation(info.getId(), "inventory"));
    }

    @Nonnull
    public INetworkItem provide(final INetworkItemHandler handler, final EntityPlayer player, final ItemStack stack, final int slotId) {
        return new NetworkItemWirelessCraftingGrid(handler, player, stack, slotId);
    }
}
